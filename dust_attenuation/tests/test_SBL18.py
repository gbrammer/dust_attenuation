def test_attenuation_SBL18_attenuate_values(x0, gamma, ampl, slope, Av):
    # get the correct values
    x, cor_vals = get_axav_cor_vals(x0, gamma, ampl, slope, Av)

    # calculate the cor_vals in fractional units
    cor_vals = np.power(10.0, -0.4 * (cor_vals))

    # initialize model
    tmodel = SBL18(x0=x0, gamma=gamma, ampl=ampl, slope=slope, Av=Av)

    # test
